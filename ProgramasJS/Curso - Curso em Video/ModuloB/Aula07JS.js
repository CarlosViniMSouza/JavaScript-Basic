// a matematica das expressoes numericas sao validas aqui no JS

/*
Simplificação:
  var n = 3
  n = n + 4 ---> n += 4 
  n = n - 5 --->  n-= 5
  n = n * 4 ---> n*= 4
  n = n / 2 ---> n /= 2
  n = n ** 2 ---> n**= 2
  n = n % 5 ---> n%= 5
*/

/*
Incremento:
  var x = 5
  x = x + 1 ---> x++
  x = x - 1 ---> x--
*/